/*-----------------------------------------------------------
 * Simple IO routines to control the LEDs.
 * This file is called ParTest.c for historic reasons.  Originally it stood for
 * PARallel port TEST.
 *-----------------------------------------------------------*/

/* Scheduler includes. */
#include "FreeRTOS.h"
#include "task.h"

/* Demo includes. */
#include "partest.h"

/* Library includes. */
#include "drivers/mss_gpio/mss_gpio.h"
#include "CMSIS/system_m2sxxx.h"

#define partstNUM_LEDS	2

/* Remember the state of the outputs for easy toggling. */
static unsigned char ucPortState = 0;

/*-----------------------------------------------------------*/

void vParTestInitialise( void )
{
long x;

	/* Initialise MSS GPIOs. */
    MSS_GPIO_init();

    /* Configure MSS GPIOs. */
    MSS_GPIO_config( MSS_GPIO_0 , MSS_GPIO_OUTPUT_MODE );
    MSS_GPIO_config( MSS_GPIO_1 , MSS_GPIO_OUTPUT_MODE );
    MSS_GPIO_config( MSS_GPIO_2 , MSS_GPIO_OUTPUT_MODE );
    MSS_GPIO_config( MSS_GPIO_3 , MSS_GPIO_OUTPUT_MODE );

    /* Ensure the LEDs are off to start. */
    for( x = 0; x < partstNUM_LEDS; x++ )
    {
    	vParTestSetLED( x, pdFALSE );
    }
}
/*-----------------------------------------------------------*/

void vParTestSetLED( unsigned portBASE_TYPE uxLED, signed portBASE_TYPE xValue )
{
	if( uxLED < partstNUM_LEDS )
	{
		taskENTER_CRITICAL();
		{
			MSS_GPIO_set_output( ( mss_gpio_id_t ) uxLED, xValue );

			/* Remember the new output state. */
			if( xValue == 0 )
			{
				ucPortState &= ~( 1 << uxLED );
			}
			else
			{
				ucPortState |= ( 1 << uxLED );
			}
		}
		taskEXIT_CRITICAL();
	}
}
/*-----------------------------------------------------------*/

void vParTestToggleLED( unsigned portBASE_TYPE uxLED )
{
	if( uxLED < partstNUM_LEDS )
	{
		taskENTER_CRITICAL();
		{
			vParTestSetLED( uxLED, !( ucPortState & ( 1 << uxLED ) ) );
		}
		taskEXIT_CRITICAL();
	}
}




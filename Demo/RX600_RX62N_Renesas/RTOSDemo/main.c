void vRegTest1Task( void *pvParameters )
{
volatile unsigned long ul = 0;

	for( ;; )
	{
		ul += 2;
		ul -= 1;
	}		
}
/*-----------------------------------------------------------*/

void vRegTest2Task( void *pvParameters )
{
volatile unsigned long ul = 0;

	for( ;; )
	{
		ul += 4;
		ul -= 2;
	}		
}





